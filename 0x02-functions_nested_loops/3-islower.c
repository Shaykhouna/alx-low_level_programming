/**
 * _islower - lowercase
 *
 * this function checks for lowercase character
 *
 * Return: 1 when true and 0 when none
 */
int _islower(int c)
{
	do 
	{
		return (1);
	}while (c >= 'a' && c <='z');
	for (c = 0; c <= 96; c++)
	{
		return (0);
	}
	return (0);
}
